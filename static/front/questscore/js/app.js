$(document).ready(function () {
    //这里面的内容就是页面装载完成后需要执行的代码
    var userNameNode = $(".mlogin");
    //需要找到button按钮，注册事件
    //需要找到文本框，注册事件
    userNameNode.keyup(function () {
        var value = userNameNode.val();
        if (value == "") {
            //让边框变成红色，并且带背景图
            userNameNode.addClass("mlogin");
        } else {
            //去掉边框和背景图
            userNameNode.removeClass("mlogin");
        }
    });
});